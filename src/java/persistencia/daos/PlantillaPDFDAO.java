    @Override
    public int insertObject(Object object) {
        PlantillaPDF plantilla = (PlantillaPDF) object;
        Connection con = null;
        int tam = 0;
        try {
            con = ConexionBD.obtenerConexion();
            String sql = "insert into plantilla_pdf(codigo,nombre) values(?,?)";
            plantilla.setCodigo(getMaxCodigo());
            PreparedStatement pS = con.prepareStatement(sql);
            pS.setInt(1, plantilla.getCodigo());
            pS.setString(2, plantilla.getNombre());
            tam = pS.executeUpdate();
            pS.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PlantillaPDFDAO.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PlantillaPDFDAO.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PlantillaPDFDAO.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            ConexionBD.cerrarConexion(con);
        }
        return tam;
    }

